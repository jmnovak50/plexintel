                THEN 'soft_structural'
            ELSE 'semantic_candidate'
        END AS label_type,
        CASE
            WHEN label_lc LIKE ANY (ARRAY[
                '%unknown%',
                '%unclear%',
                '%mixed%',
                '%generic%'
            ]) THEN false
            WHEN label_lc LIKE ANY (ARRAY[
                '%rating%',
                '%released in%',
                '%recent%',
                '%2025%',
                '%2026%'
            ]) THEN false
            WHEN label_lc IN (
                'tv series episode',
                'tv series episodes',
                'tv episodes',
                'tv episode',
                'television episode',
                'television episodes',
                'television series episodes',
                'feature-length movies',
                'feature length movies',
                'feature films',
                'feature film preference',
                'feature-film movies',
                'standalone feature films',
                'single tv episode',
                'single television episode',
                'single-episode installments',
                'single-episode tv installments',
