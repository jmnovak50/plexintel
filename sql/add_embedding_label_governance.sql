-- Embedding label governance for explanation display only.
--
-- embedding_labels.dimension intentionally stores exact combined SHAP/model
-- dimensions. Dimensions 0-767 are media-side labels and 768-1535 are
-- user-side labels. This migration does not normalize dimensions and does not
-- change model training, scoring, SHAP generation, or embedding generation.
-- It only adds governance metadata so user-facing explanations can filter
-- weak or structural labels while preserving the raw label rows.

ALTER TABLE public.embedding_labels
    ADD COLUMN IF NOT EXISTS label_type text,
    ADD COLUMN IF NOT EXISTS explainable boolean,
    ADD COLUMN IF NOT EXISTS display_label text,
    ADD COLUMN IF NOT EXISTS needs_review boolean DEFAULT false,
    ADD COLUMN IF NOT EXISTS updated_at timestamp without time zone DEFAULT now(),
    ADD COLUMN IF NOT EXISTS last_reviewed_at timestamp without time zone,
    ADD COLUMN IF NOT EXISTS review_attempt_count integer NOT NULL DEFAULT 0,
    ADD COLUMN IF NOT EXISTS next_review_at timestamp without time zone;

CREATE TABLE IF NOT EXISTS public.embedding_label_history (
    history_id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dimension integer NOT NULL,
    old_label text,
    old_label_type text,
    old_explainable boolean,
    old_display_label text,
    old_needs_review boolean,
    old_last_reviewed_at timestamp without time zone,
    old_review_attempt_count integer,
    old_next_review_at timestamp without time zone,
    new_label text,
    change_reason text,
    changed_at timestamp without time zone DEFAULT now()
);

ALTER TABLE IF EXISTS public.embedding_label_history
    ADD COLUMN IF NOT EXISTS old_needs_review boolean,
    ADD COLUMN IF NOT EXISTS old_last_reviewed_at timestamp without time zone,
    ADD COLUMN IF NOT EXISTS old_review_attempt_count integer,
    ADD COLUMN IF NOT EXISTS old_next_review_at timestamp without time zone;

CREATE INDEX IF NOT EXISTS idx_embedding_label_history_dimension
    ON public.embedding_label_history (dimension, changed_at DESC);

COMMENT ON COLUMN public.embedding_labels.dimension IS
